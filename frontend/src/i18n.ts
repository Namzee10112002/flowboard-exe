function formatTemplate(input: string, params?: Record<string, string | number>): string {
  if (!params) return input;
  return input.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, key: string) => {
    const v = params[key];
    return v === undefined || v === null ? "" : String(v);
  });
}

export type I18nKey = keyof typeof dict.en;

export function t(key: I18nKey, params?: Record<string, string | number>): string {
  const locale = useSettingsStore.getState().locale;
  const base = dict[locale]?.[key] ?? dict.en[key];
  return formatTemplate(base, params);
}

export function tierLabel(tier: string | null | undefined): string {
  if (tier === "PAYGATE_TIER_TWO") return "Ultra";
  if (tier === "PAYGATE_TIER_ONE") return "Pro";
  return t("settingsTierDetecting");
}
